    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
         try {
        String diem = txtdiem.getText().trim();  // Assuming this is the primary key, idgiaovien
        String sv = cbsv.getSelectedItem().toString();
        String mon = cbmon.getSelectedItem().toString();
        String cc = txtcc.getText().trim();
        String gk = txtgk.getText().trim();
        String ck = txtck.getText().trim();
        String dtb = txtdtb.getText().trim();
        String ma = sinhvien.get(sv);

        // Kết nối DB
        con = ConnectDB.KetnoiDB();

        // Sử dụng PreparedStatement để đảm bảo an toàn và chính xác
        String sql = "UPDATE giaovien SET hoten = ?, ngaysinh = ?, gioitinh = ?, diachi = ?, sodienthoai = ?, email = ?, khoa = ? WHERE idgiaovien = ?";
        PreparedStatement st = con.prepareStatement(sql);

        // Set the values for the placeholders
        st.setString(1, diem);
        st.setString(3, sv);
        st.setString(4, mon);
        st.setString(5, cc);
        st.setString(6, gk);
        st.setString(7, ck);
        st.setString(8, dtb);  // Set the idgiaovien as the condition for the WHERE clause

        // Thực hiện câu lệnh cập nhật
        st.executeUpdate();

        // Đóng kết nối
        con.close();

        // Thông báo khi cập nhật thành công
        JOptionPane.showMessageDialog(this, "Sửa thành công!");
        load_diem();
    } catch (Exception ex) {
        ex.printStackTrace();
    }
    }//GEN-LAST:event_btnSuaActionPerformed
