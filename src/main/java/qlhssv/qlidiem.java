    private void tbdiemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbdiemMouseClicked
        int i=tbdiem.getSelectedRow();
        DefaultTableModel tb=(DefaultTableModel)tbdiem.getModel();
        txtdiem.setText(tb.getValueAt(i, 0).toString());
        cbsv.setSelectedItem(tb.getValueAt(i, 1).toString());
        cbmon.setSelectedItem(tb.getValueAt(i, 3).toString());
        txtcc.setText(tb.getValueAt(i, 4).toString());
        txtgk.setText(tb.getValueAt(i, 5).toString());
        txtck.setText(tb.getValueAt(i, 6).toString());
        txtdtb.setText(tb.getValueAt(i, 6).toString());
        float diemCC = Float.parseFloat(txtcc.getText());
        float diemGK = Float.parseFloat(txtgk.getText());
        float diemCK = Float.parseFloat(txtck.getText());
        
        float diemTB = (diemCC * 0.1f) + (diemGK * 0.3f) + (diemCK * 0.6f);
        txtdtb.setText(String.valueOf(diemTB));
    }//GEN-LAST:event_tbdiemMouseClicked

