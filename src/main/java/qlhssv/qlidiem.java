 private void load_diem(){
     try {
        con=ConnectDB.KetnoiDB();
        String sql="Select*From diem";
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery(sql);
        String[] tieude={"ID Điểm","ID Sinh Viên","ID Môn học","Điểm chuyên cần","Điểm giữa kì","Điểm cuối kì","Điểm trung bình"};
        DefaultTableModel tb = new DefaultTableModel(tieude,0);
        while(rs.next()){
            Vector v = new Vector();
             v.add(rs.getString("iddiem"));
              v.add(rs.getString("idsinhvien"));
              v.add(rs.getString("idmonhoc"));
              v.add(rs.getString("diem_chuyencan"));
               v.add(rs.getString("diem_giua_ky"));
              v.add(rs.getString("diem_cuoi_ky"));
              v.add(rs.getString("diem_trung_binh"));
              tb.addRow(v);
        }
        tbdiem.setModel(tb);
        con.close();
    } catch (Exception ex) {
        ex.printStackTrace();
    }
 }

